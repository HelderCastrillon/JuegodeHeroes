    private void btnVerUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerUsuarioActionPerformed
        // TODO add your handling code here:
       UsuarioDao usuarios = null;
        try {
            List<Usuarios> listaUsuarios = usuarios.getUsuarios();
            DefaultListModel modelo = new DefaultListModel();
            for(int i = 0; i<listaUsuarios.size(); i++){
                    modelo.addElement(listaUsuarios.get(i).getNombre());
            }
            lstUsuarios.setModel(modelo);
        } catch (SQLException ex) {
            Logger.getLogger(jfBuscar.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_btnVerUsuarioActionPerformed

